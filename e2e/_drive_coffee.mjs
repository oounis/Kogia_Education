ok(t.includes('Paiement à la livraison') && !t.includes('e-mail') && !t.toLowerCase().includes('email'), 'checkout COD une page : ni e-mail ni compte')
await p.getByRole('button', { name: /Confirmer ·/ }).last().click(); await p.waitForTimeout(1000)
