await p.getByRole('button', { name: /Ajouter au panier/ }).click(); await p.waitForTimeout(600)
await p.getByRole('button', { name: /Panier/ }).first().click(); await p.waitForTimeout(500)
await p.getByRole('button', { name: /Commander · paiement/ }).click(); await p.waitForTimeout(900)
const inputs = p.locator('input')
await inputs.nth(0).fill('Client Test')
await inputs.nth(1).fill('+216 20 123 456')
await p.locator('textarea, input').last()
await p.getByPlaceholder(/Rue, immeuble/).fill('12 rue des Jasmins, Houmt Souk')
