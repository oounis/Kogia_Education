ok(t.includes('La tradition —'), 'la fiche raconte LA TRADITION (plus de « bienfait » illégal)')
ok(t.includes('proportions restent le secret'), 'la Signature garde ses proportions secrètes')
ok(t.includes('orge (gluten)'), "l'allergène ORGE-GLUTEN est déclaré")
